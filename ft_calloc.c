#include "libft.h"

void *ft_calloc(size_t nitems, size_t size)
{
	char *ptr;
	size_t i;
	
	i = 0;
	ptr = malloc(nitems * size);
	if (ptr == NULL)
		return (ptr);
	while (i < nitems * size)
	{
		ptr[i] = 0;
		i++;
	}
	return (ptr);
}

